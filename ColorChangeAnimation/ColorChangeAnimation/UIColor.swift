import Foundation
import UIKit

extension UIColor {
    
    //MARK: Class Methods
    
    public class func TCTealColor() -> UIColor {
        return UIColor(red: (26.0/256), green: (188.0/256.0), blue: (156.0/256.0), alpha: 0.8)
    }
    
    public class func TCGreenColor() -> UIColor {
        return UIColor(red: 46.0/256.0, green: 204.0/256.0, blue: 113.0/256.0, alpha: 0.8)
    }
    
    public class func TCBlueColor() -> UIColor {
        return UIColor(red: 52.0/256.0, green: 152.0/256.0, blue: 219.0/256.0, alpha: 0.8)
    }
    
    public class func TCPurpleColor() -> UIColor {
        return UIColor(red: 155.0/256.0, green: 89.0/256.0, blue: 182.0/256.0, alpha: 0.8)
    }
    
    public class func TCRedColor() -> UIColor {
        return UIColor(red: 193.0/256.0, green: 73.0/256.0, blue: 94.0/256.0, alpha: 0.8)
    }
    
    public class func TCOrangeColor() -> UIColor {
        return UIColor(red: 255.0/256.0, green: 95.0/256.0, blue: 55.0/256.0, alpha: 0.8)
    }
    

}