import Foundation
import UIKit


extension UIFont {
    
    //MARK: Class Methods
    public class func TCHeadingLarge() -> UIFont {
        return UIFont(name: "Quicksand-Regular", size: 40.0)!
    }
    public class func TCHeadingBoldMedium() -> UIFont {
        return UIFont(name: "Quicksand-Bold", size: 20.0)!
    }

}